<template>
  <div class="hello">
    <h1>{{ msg }}</h1>
    <canvas id="myChart" width="400" height="150"></canvas>
  </div>
</template>

<script>
import Chart from 'chart.js/auto';

export default {
  name: 'OptionsChart',
  props: {
    msg: String
  },
  mounted() {
    console.log('CAM in mounted')
    const data = [
      { year: 2010, count: 10 },
      { year: 2011, count: 20 },
      { year: 2012, count: 15 },
      { year: 2013, count: 25 },
      { year: 2014, count: 22 },
      { year: 2015, count: 30 },
      { year: 2016, count: 20 },
    ];

    new Chart(
      document.getElementById('myChart'),
      {
        type: 'bar',
        data: {
          labels: data.map(row => row.year),
          datasets: [
            {
              label: 'Acquisitions by year',
              data: data.map(row => row.count)
            }
          ]
        }
      }
    );
  }
}
</script>

